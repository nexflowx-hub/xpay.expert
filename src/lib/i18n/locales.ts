export function localeFromTimezone(): Locale | null {
  if (typeof Intl === "undefined") return null;
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    return tzCountry[tz] ?? null;
  } catch {
    return null;
  }
}

